# Type of machine learning -
# 1.Supervised Learning - ideal for tasks with label data where the desired output is known.
# 2.UnSupervised Learning - suitable for identifying pattern in a unlabeled data without explicit guidance.
# 3.ReinForcement Learning - Best for training agent to make decision through interaction  with an environment 




